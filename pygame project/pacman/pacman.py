def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    "Move pacman and all ghosts."
    writer.undo()  # for removing the previous timer and write the new timer
    writer.write(state['score'])  

    clear()

    if valid(pacman + aim):  #if movement is valid then move the pacman
        pacman.move(aim)

    index = offset(pacman)  
    
    if tiles[index] == 1:  #for restricting the timer speed, you can skip this if statement and use only the score increasing statement
        tiles[index] = 2
        state['score'] += 1  #keep the timer increasing
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)
        
    up()
    goto(pacman.x + 10, pacman.y + 10)  #goto the pacman position and draw yellow pacman of size 20
    dot(20, 'yellow')

    for point, course in ghosts:  #this loop is to move the ghost balls 
        if valid(point + course): #if the point where the ball is to move is valid then move the ghost ball
            point.move(course)
        else:                     
            options = [  #creating options in each direction
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)  #choosing the direction to move randomly
            course.x = plan.x  #copy the plan coordinates to course
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)  #move to the position and make red ball at that position
        dot(20, 'red')

    update()

    for point, course in ghosts:  #checking for pacman and ghost ball colliding if yes then game over
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

def change(x, y):
    "Change pacman aim if valid."
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

